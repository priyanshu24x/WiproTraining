class Calculations:
    @staticmethod
    def add(n1,n2):
        return n1 + n2
    @staticmethod
    def sub(n1,n2):
        return n1 -n2
    @staticmethod
    def mult(n1,n2):
        return n1*n2
    @staticmethod
    def div(n1,n2):
        return n1/n2
    @staticmethod
    def ne(n1,n2):
        if n1==n2:
            return True
        else:
            return False
